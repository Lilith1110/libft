#include <ctype.h>

void	ft_bzero(void *s, size_t n)
{
	char	*aux;
	size_t	i;

	if (n != 0)
	{
		aux = s;
		i = 0;
		while (i < n)
		{
			*(aux + i) = '0';
			i++;
		}
		s = aux;
	}
}
